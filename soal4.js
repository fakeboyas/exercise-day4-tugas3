let move = "down";

switch (move) {
  case "up":
    console.log("Karakter berjalan ke atas");
    break;
  case "right":
    console.log("Karakter berjalan ke kanan");
    break;
  case "left":
    console.log("Karakter berjalan ke kiri");
    break;
  case "down":
    console.log("Karakter berjalan ke bawah");
  default:
    break;
}
